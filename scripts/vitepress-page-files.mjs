import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const currentFile = fileURLToPath(import.meta.url)
const repoRoot = path.resolve(path.dirname(currentFile), '..')
const generatedMarker = '<!-- Generated by scripts/sync-vitepress-pages.mjs; source: '
const ignoredDirectories = new Set(['.git', '.vitepress', 'node_modules'])

let cachedAliasMap
let cachedSourceFiles

export function buildSourceExcludes() {
  return [...collectReadmeFiles(), ...buildAliasMap().keys()].sort(compareAlpha)
}

export function getVitePressSourcePath(relativePath) {
  const normalized = normalizeRelativePath(relativePath)

  if (path.posix.basename(normalized) === 'README.md') {
    const directory = path.posix.dirname(normalized)
    return directory === '.' ? 'index.md' : `${directory}/index.md`
  }

  return buildAliasMap().get(normalized) ?? normalized
}

export function isGeneratedAliasPath(relativePath) {
  const normalized = normalizeRelativePath(relativePath)
  return [...buildAliasMap().values()].includes(normalized)
}

export function syncGeneratedPages() {
  for (const [sourceRelativePath, targetRelativePath] of buildAliasMap()) {
    const targetAbsolutePath = path.join(repoRoot, targetRelativePath)
    const sourceFileName = path.posix.basename(sourceRelativePath)
    const content = `${generatedMarker}${sourceRelativePath} -->\n<!--@include: ./${sourceFileName}-->\n`

    fs.mkdirSync(path.dirname(targetAbsolutePath), { recursive: true })
    fs.writeFileSync(targetAbsolutePath, content, 'utf8')
  }
}

function buildAliasMap() {
  if (cachedAliasMap) {
    return cachedAliasMap
  }

  const sourceFiles = collectSourceFiles()
  const aliasMap = new Map()
  const reservedTargets = new Set(sourceFiles.filter((file) => !needsRouteAlias(path.posix.basename(file))))

  for (const sourceRelativePath of sourceFiles.filter(isAliasSourceFile).sort(compareAlpha)) {
    const targetRelativePath = resolveAliasTarget(sourceRelativePath, reservedTargets)

    aliasMap.set(sourceRelativePath, targetRelativePath)
    reservedTargets.add(targetRelativePath)
  }

  cachedAliasMap = aliasMap
  return aliasMap
}

function resolveAliasTarget(sourceRelativePath, reservedTargets) {
  const directory = path.posix.dirname(sourceRelativePath)
  const sourceFileName = path.posix.basename(sourceRelativePath)
  const baseName = sourceFileName.replace(/\.md$/, '')
  const safeBaseName = sanitizeUnsafeFileName(baseName)
  const directoryPrefix = directory === '.' ? '' : `${directory}/`
  const candidates = [`${safeBaseName}.md`, `${safeBaseName}.vitepress.md`]

  for (const candidateFileName of candidates) {
    const targetRelativePath = `${directoryPrefix}${candidateFileName}`

    if (targetRelativePath === sourceRelativePath) {
      continue
    }

    if (!reservedTargets.has(targetRelativePath)) {
      return targetRelativePath
    }

    if (isManagedAliasTarget(targetRelativePath, sourceRelativePath)) {
      return targetRelativePath
    }
  }

  let counter = 2

  while (true) {
    const targetRelativePath = `${directoryPrefix}${safeBaseName}.${counter}.md`

    if (!reservedTargets.has(targetRelativePath) || isManagedAliasTarget(targetRelativePath, sourceRelativePath)) {
      return targetRelativePath
    }

    counter += 1
  }
}

function sanitizeUnsafeFileName(baseName) {
  const withoutBracketTokens = baseName.replace(/\[([^\]]+)\]\s*/g, '$1 ')
  const withoutHashTokens = withoutBracketTokens.replace(/#/g, ' ')
  const squashedWhitespace = withoutHashTokens.replace(/\s+/g, ' ').trim()
  return squashedWhitespace || 'page'
}

function isManagedAliasTarget(targetRelativePath, sourceRelativePath) {
  const absoluteTargetPath = path.join(repoRoot, targetRelativePath)

  if (!fs.existsSync(absoluteTargetPath)) {
    return false
  }

  const content = fs.readFileSync(absoluteTargetPath, 'utf8')
  return content.startsWith(`${generatedMarker}${sourceRelativePath} -->`)
}

function collectReadmeFiles() {
  return collectSourceFiles().filter((file) => path.posix.basename(file) === 'README.md')
}

function collectSourceFiles() {
  if (cachedSourceFiles) {
    return cachedSourceFiles
  }

  cachedSourceFiles = walkMarkdownFiles(repoRoot)
  return cachedSourceFiles
}

function walkMarkdownFiles(absoluteDirectory, relativeDirectory = '') {
  const files = []

  for (const entry of fs.readdirSync(absoluteDirectory, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (ignoredDirectories.has(entry.name)) {
        continue
      }

      const nextRelativeDirectory = relativeDirectory ? `${relativeDirectory}/${entry.name}` : entry.name
      files.push(...walkMarkdownFiles(path.join(absoluteDirectory, entry.name), nextRelativeDirectory))
      continue
    }

    if (!entry.isFile() || !entry.name.endsWith('.md')) {
      continue
    }

    const relativePath = relativeDirectory ? `${relativeDirectory}/${entry.name}` : entry.name
    files.push(relativePath)
  }

  return files.sort(compareAlpha)
}

function isAliasSourceFile(relativePath) {
  return needsRouteAlias(path.posix.basename(relativePath))
}

function needsRouteAlias(fileName) {
  return /\[[^\]]+\]/.test(fileName) || fileName.includes('#')
}

function normalizeRelativePath(relativePath) {
  return relativePath.split(path.sep).join('/')
}

function compareAlpha(left, right) {
  return left.localeCompare(right, 'ko', { numeric: true, sensitivity: 'base' })
}
